#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*new;
	int		i;

	i = 0;
	new = (char *)s;
	while (n > 0)
	{
		new[i] = (char)c;
		n--;
		i++;
	}
	return (new);
}
